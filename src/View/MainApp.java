    void formSarpras(){
        
        konek = new Koneksi();
        con = konek.getConnection();
        try{
            int row = tbl_sarpras.getSelectedRow();
            String klik_tbl = (tbl_sarpras.getModel().getValueAt(row, 0).toString());
            String query = "SELECT * FROM sarana WHERE kd_srp='"+klik_tbl+"'";
            st = con.createStatement();
            rs=st.executeQuery(query);
            if(rs.next()){
                showEditForm();
                String kode = rs.getString("kd_srp");
                kdSarpras.setText(kode);
                String nm = rs.getString("nama_srp");
                nm_sarpras.setText(nm);
                String jmlh = rs.getString("jumlah");
                jumlah_sarpras.setText(jmlh);
                String kondisis = rs.getString("kondisi");
                kondisi_sarpras.setSelectedItem(kondisis);
                String jenis = rs.getString("jenis");
                jenis_sarpras.setSelectedItem(jenis);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e );
        }
    }

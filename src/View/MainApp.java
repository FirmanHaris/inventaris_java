    private void btnEditSrpMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditSrpMouseClicked
        // TODO add your handling code here:
        String kd = kdSarpras.getText();
        String nm = nm_sarpras.getText();
        String jumlah = jumlah_sarpras.getText();
        int jmlh = Integer.parseInt(jumlah);
        Object kondisi = kondisi_sarpras.getSelectedItem();
        Object jenis = jenis_sarpras.getSelectedItem();
        Sarpras sr = new SarprasController();
        sr.Edit(kd, nm, jmlh, kondisi, jenis);
        content.removeAll();
        content.repaint();
        content.revalidate();
        
        content.add(sarprasPanel);
        content.repaint();
        content.revalidate();
        table_sarpras();
    }//GEN-LAST:event_btnEditSrpMouseClicked

